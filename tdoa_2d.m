function [ x1, y1, x2, y2 ] = tdoa_2d( tdoa_12, tdoa_13 )
    sspeed = 34000.29; % [cm/s]
    smplfreq = 96153; % [Hz]
    vt_12 = ((tdoa_12 - 7) / smplfreq) * sspeed;
    vt_13 = (((tdoa_13 - 7) / smplfreq) - 2/smplfreq) * sspeed;
    len = 20;
    m1 = [0, 0, 0];
    m2 = [-(len.^2 - (len/2).^2).^0.5, len/2, 0];
    m3 = [-(len.^2 - (len/2).^2).^0.5, -len/2, 0];
    
    M = -([m3(1, 2), -m2(1, 2); ...
          -m3(1, 1), m2(1, 1)]);
    M = (1/(m2(1, 1)*m3(1, 2) - m2(1, 2)*m3(1, 1))).*M; 
    
    V = [vt_12; ...
         vt_13];
     
    K = (0.5).*[vt_12.^2 - m2(1, 1).^2 - m2(1, 2).^2; ...
         vt_13.^2 - m3(1, 1).^2 - m3(1, 2).^2; 
         ];
     
    H = M*V;
    C = M*K;
    
    a = H(1, 1).^2 + H(2, 1).^2 - 1;
    b = 2*(H(1, 1)*C(1, 1) + H(2, 1)*C(2, 1));
    c = C(1, 1).^2 + C(2, 1).^2;
    
    r1 = (-b + (b.^2 - 4*a*c)^(1/2))/(2*a);
    r2 = (-b - (b.^2 - 4*a*c)^(1/2))/(2*a);
    
    x1 = H(1, 1) * r1 + C(1, 1);
    y1 = H(2, 1) * r1 + C(2, 1);
    x2 = H(1, 1) * r2 + C(1, 1);
    y2 = H(2, 1) * r2 + C(2, 1);
end

